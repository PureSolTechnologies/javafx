package com.puresoltechnologies.javafx.charts.axes;

/**
 * Defines the type of the axis.
 * 
 * @author Rick-Rainer Ludwig
 *
 */
public enum AxisType {
    /**
     * Primary X axis, normally located at the bottom of the plot.
     */
    X,
    /**
     * Primary Y axis, normally located at the left side of the plot.
     */
    Y,
    /**
     * Alternative X axis, normally located at the top of the plot.
     */
    ALT_X,
    /**
     * Alternative Y axis, normally located at the right side of the plot.
     */
    ALT_Y;
}
