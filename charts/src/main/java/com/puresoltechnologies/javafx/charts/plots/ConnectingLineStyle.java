package com.puresoltechnologies.javafx.charts.plots;

/**
 * This enum is used to specify how points in a plot are conntected to each
 * other to make trends and difference visible.
 *
 * @author Rick-Rainer Ludwig
 *
 */
public enum ConnectingLineStyle {

    /**
     * This constant tells to not connect points.
     */
    NONE, //
    /**
     * This constant tells to connect points with straight lines to each other.
     */
    STRAIGHT_LINE, //
    ;

}
